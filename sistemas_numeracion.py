# Profundizando Sistemas Numeraci√≥n
# Decimal
a = 10
print(f'a: {a}')
# Binario, la b es de binary
a = 0b1010
print(f'a: {a}')
# Octal
a = 0o12
print(f'a: {a}')
# Hexadecimal, siempre empieza por 0x
a = 0xA
print(f'a: {a}')

# Convertir un tipo entero, incluyendo base
# Decimal
a = int('23', 10)
# Binario
a = int('10111', 2)
print(f'a: {a}')
# Octal
a = int('27', 8)
print(f'a: {a}')
# Hexadecimal
a = int('17', 16)
print(f'a: {a}')
# Base 5
a = int('344', 5)
print(f'a: {a}')
